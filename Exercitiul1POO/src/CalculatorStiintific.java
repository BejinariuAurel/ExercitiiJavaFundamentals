public class CalculatorStiintific extends Calculator {

   public CalculatorStiintific (){
   }
   public static double ridicareLaPutere(int a, int b){
       return Math.pow(a,b);

   }
   public static double radicalulNumarului(int a){
       return Math.sqrt(a);
   }
   public double logaritm10(int a){
       return Math.log10(a);
   }
   public double logaritm(int a) {
       return Math.log(a);
   }

}
